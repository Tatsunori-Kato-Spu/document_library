import { useState, useEffect } from "react";
import { FontAwesomeIcon } from "@fortawesome/react-fontawesome";
import { faEnvelope, faStar } from "@fortawesome/free-solid-svg-icons";
import { faStar as faRegStar } from "@fortawesome/free-regular-svg-icons";
import "./page.css";
import Searchbar from "../Searchbar/Searchbar";
import Actiondropdown from "../actiondropdown/actiondropdown";
import Dropdown from "react-bootstrap/Dropdown";
import ButtonUpload from "../AddDoc/buttonupload";
import Header from "../../Layout/Header/Header";
import { useNavigate } from "react-router-dom";

const Pagedoc = ({ userRole }) => {
  // ‡∏ï‡∏±‡∏ß‡πÅ‡∏õ‡∏£‡∏™‡∏≥‡∏´‡∏£‡∏±‡∏ö‡∏à‡∏±‡∏î‡∏Å‡∏≤‡∏£‡∏™‡∏ñ‡∏≤‡∏ô‡∏∞ (State variables)
  const [filteredData, setFilteredData] = useState([]);
  const [sortOrder, setSortOrder] = useState("desc");
  const [showModal, setShowModal] = useState(false);
  const [selectedDoc, setSelectedDoc] = useState(null);
  const navigate = useNavigate();

  const [username, setUsername] = useState("");

  useEffect(() => {
    const storedUsername = localStorage.getItem("username");

    if (storedUsername) {
      setUsername(storedUsername);
    } else {
      console.error("Username not found in storage");
    }
  }, []);

  // ‡∏ü‡∏±‡∏á‡∏Å‡πå‡∏ä‡∏±‡∏ô‡∏™‡∏≥‡∏´‡∏£‡∏±‡∏ö‡∏î‡∏∂‡∏á‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏•‡πÄ‡∏≠‡∏Å‡∏™‡∏≤‡∏£‡∏à‡∏≤‡∏Å API
  useEffect(() => {
    const fetchDocuments = async () => {
      try {
        const response = await fetch(
          `http://localhost:3001/api/documents?username=${username}`
        );
        const data = await response.json();
        setFilteredData(data); // ‡∏≠‡∏±‡∏û‡πÄ‡∏î‡∏ï state
      } catch (error) {
        console.error("Error fetching documents:", error);
      }
    };
    if (username) {
      fetchDocuments(); // ‡πÄ‡∏£‡∏µ‡∏¢‡∏Å‡∏ü‡∏±‡∏á‡∏Å‡πå‡∏ä‡∏±‡∏ô fetchDocuments ‡πÄ‡∏°‡∏∑‡πà‡∏≠ username ‡∏ñ‡∏π‡∏Å‡∏ï‡∏±‡πâ‡∏á‡∏Ñ‡πà‡∏≤
    } else {
      console.log("Username is missing or incorrect");
    }
  }, [username]);

  // ‡∏ü‡∏±‡∏á‡∏Å‡πå‡∏ä‡∏±‡∏ô‡∏™‡∏≥‡∏´‡∏£‡∏±‡∏ö‡∏Ñ‡πâ‡∏ô‡∏´‡∏≤‡πÄ‡∏≠‡∏Å‡∏™‡∏≤‡∏£
  const handleSearch = (filteredDocuments) => {
    setFilteredData(filteredDocuments);
  };



  // ‡∏ü‡∏±‡∏á‡∏Å‡πå‡∏ä‡∏±‡∏ô‡∏™‡∏≥‡∏´‡∏£‡∏±‡∏ö‡∏à‡∏±‡∏î‡πÄ‡∏£‡∏µ‡∏¢‡∏á‡∏ï‡∏≤‡∏°‡∏ß‡∏±‡∏ô‡∏ó‡∏µ‡πà
  const handleSortByDate = () => {
    const sortedData = [...filteredData].sort((a, b) => {
      const dateA = new Date(a["doc_date"].split("/").reverse().join("-"));
      const dateB = new Date(b["doc_date"].split("/").reverse().join("-"));
      return sortOrder === "desc" ? dateB - dateA : dateA - dateB;
    });
    setFilteredData(sortedData);
    setSortOrder(sortOrder === "desc" ? "asc" : "desc");
  };

  // ‡∏ü‡∏±‡∏á‡∏Å‡πå‡∏ä‡∏±‡∏ô‡∏™‡∏≥‡∏´‡∏£‡∏±‡∏ö‡∏à‡∏±‡∏î‡πÄ‡∏£‡∏µ‡∏¢‡∏á‡∏ï‡∏≤‡∏°‡∏•‡∏≥‡∏î‡∏±‡∏ö
  const handleSortByOrder = () => {
    const sortedData = [...filteredData].sort((a, b) => {
      const orderA = parseInt(a["‡∏´‡∏°‡∏≤‡∏¢‡πÄ‡∏•‡∏Ç"]);
      const orderB = parseInt(b["‡∏´‡∏°‡∏≤‡∏¢‡πÄ‡∏•‡∏Ç"]);
      return sortOrder === "desc" ? orderB - orderA : orderA - orderB;
    });
    setFilteredData(sortedData);
    setSortOrder(sortOrder === "desc" ? "asc" : "desc");
  };

  // ‡∏ü‡∏±‡∏á‡∏Å‡πå‡∏ä‡∏±‡∏ô‡∏™‡∏≥‡∏´‡∏£‡∏±‡∏ö‡∏Ñ‡∏•‡∏¥‡∏Å‡πÑ‡∏≠‡∏Ñ‡∏≠‡∏ô‡∏î‡∏≤‡∏ß‡πÄ‡∏û‡∏∑‡πà‡∏≠‡∏ó‡∏≥‡πÄ‡∏Ñ‡∏£‡∏∑‡πà‡∏≠‡∏á‡∏´‡∏°‡∏≤‡∏¢‡πÄ‡∏õ‡πá‡∏ô Favorite
  const handleStarClick = (index) => {
    const newData = [...filteredData];
    newData[index].isFavorite = !newData[index].isFavorite;
    setFilteredData(newData);
  };

  // ‡∏ü‡∏±‡∏á‡∏Å‡πå‡∏ä‡∏±‡∏ô‡πÄ‡∏°‡∏∑‡πà‡∏≠‡∏Ñ‡∏•‡∏¥‡∏Å‡∏ó‡∏µ‡πà‡πÅ‡∏ñ‡∏ß (‡πÄ‡∏õ‡∏•‡∏µ‡πà‡∏¢‡∏ô‡∏™‡∏ñ‡∏≤‡∏ô‡∏∞‡πÄ‡∏õ‡πá‡∏ô "‡∏≠‡πà‡∏≤‡∏ô‡πÅ‡∏•‡πâ‡∏ß")
  const handleRowClick = (item) => {
    const newData = [...filteredData];
    const index = filteredData.findIndex(
      (doc) => doc["‡∏´‡∏°‡∏≤‡∏¢‡πÄ‡∏•‡∏Ç"] === item["‡∏´‡∏°‡∏≤‡∏¢‡πÄ‡∏•‡∏Ç"]
    );
    newData[index].isRead = true;
    setFilteredData(newData);
  };

  // ‡∏ü‡∏±‡∏á‡∏Å‡πå‡∏ä‡∏±‡∏ô‡πÅ‡∏™‡∏î‡∏á Modal ‡∏™‡∏≥‡∏´‡∏£‡∏±‡∏ö‡∏•‡∏ö‡πÄ‡∏≠‡∏Å‡∏™‡∏≤‡∏£
  const handleShowModal = (doc) => {
    setSelectedDoc(doc);
    setShowModal(true);
  };

  const handleDelete = async () => {
    try {
      const response = await fetch(
        `http://localhost:3001/api/documents/${selectedDoc.doc_number}`,
        {
          method: "DELETE",
        }
      );

      if (response.ok) {
        setFilteredData((prev) =>
          prev.filter((doc) => doc.doc_number !== selectedDoc.doc_number)
        );
      } else {
        console.error("‡∏•‡∏ö‡πÑ‡∏°‡πà‡∏™‡∏≥‡πÄ‡∏£‡πá‡∏à:", await response.json());
      }
    } catch (err) {
      console.error("‡πÄ‡∏Å‡∏¥‡∏î‡∏Ç‡πâ‡∏≠‡∏ú‡∏¥‡∏î‡∏û‡∏•‡∏≤‡∏î‡∏ï‡∏≠‡∏ô‡∏•‡∏ö:", err);
    }

    setShowModal(false);
    setSelectedDoc(null);
  };

  // ‡∏ü‡∏±‡∏á‡∏Å‡πå‡∏ä‡∏±‡∏ô‡∏™‡∏≥‡∏´‡∏£‡∏±‡∏ö‡∏î‡∏≤‡∏ß‡∏ô‡πå‡πÇ‡∏´‡∏•‡∏î‡πÄ‡∏≠‡∏Å‡∏™‡∏≤‡∏£
  const handleDownload = (docId) => {
    const fileUrl = `/files/${docId}.pdf`;
    const link = document.createElement("a");
    link.href = fileUrl;
    link.download = `${docId}.pdf`;
    document.body.appendChild(link); // ‡πÄ‡∏û‡∏¥‡πà‡∏°‡∏•‡∏¥‡∏á‡∏Å‡πå‡∏•‡∏á‡πÉ‡∏ô DOM
    link.click(); // ‡∏Å‡∏£‡∏∞‡∏ï‡∏∏‡πâ‡∏ô‡∏Å‡∏≤‡∏£‡∏Ñ‡∏•‡∏¥‡∏Å
    document.body.removeChild(link); // ‡∏•‡∏ö‡∏•‡∏¥‡∏á‡∏Å‡πå‡∏≠‡∏≠‡∏Å‡∏à‡∏≤‡∏Å DOM
  };

  return (
    <>
      <div className="page-container">
        <Header />
        {/* ‡∏™‡πà‡∏ß‡∏ô‡∏Ç‡∏≠‡∏á‡πÅ‡∏ñ‡∏ö‡∏Ñ‡πâ‡∏ô‡∏´‡∏≤ */}
        <div className="searchbar-container">
          <Searchbar onSearch={handleSearch} searchType="documents" />
        </div>

        {/* ‡∏ï‡∏≤‡∏£‡∏≤‡∏á‡πÅ‡∏™‡∏î‡∏á‡πÄ‡∏≠‡∏Å‡∏™‡∏≤‡∏£ */}
        <div className="table-wrapper">
          <table className="table-contenner">
            <thead className="table-th">
              <tr>
                <th></th>
                <th>
                  ‡∏•‡∏≥‡∏î‡∏±‡∏ö
                  <button className="icon-button" onClick={handleSortByOrder}>
                    {sortOrder === "desc" ? "üîΩ" : "üîº"}
                  </button>
                </th>
                <th>‡∏´‡∏°‡∏≤‡∏¢‡πÄ‡∏•‡∏Ç</th>
                <th>‡∏ä‡∏∑‡πà‡∏≠‡πÄ‡∏≠‡∏Å‡∏™‡∏≤‡∏£</th>
                <th>‡πÄ‡∏£‡∏∑‡πà‡∏≠‡∏á</th>
                <th>‡∏´‡∏ô‡πà‡∏ß‡∏¢‡∏á‡∏≤‡∏ô</th>
                <th>
                  ‡∏ß‡∏±‡∏ô‡∏ó‡∏µ‡πà
                  <button className="icon-button" onClick={handleSortByDate}>
                    {sortOrder === "desc" ? "üîΩ" : "üîº"}
                  </button>
                </th>
                <th>‡πÄ‡∏ß‡∏•‡∏≤</th>
                <th></th>
              </tr>
            </thead>

            <tbody>
              {filteredData.length > 0 ? (
                filteredData.map((item, index) => (
                  <tr
                    key={index}
                    className={item.isRead ? "row-read" : "row-unread"}
                    onClick={() => handleRowClick(item)}
                  >
                    {/* ‡∏Ñ‡∏≠‡∏•‡∏±‡∏°‡∏ô‡πå Favorite */}
                    <td>
                      <FontAwesomeIcon
                        icon={item.isFavorite ? faStar : faRegStar}
                        style={{
                          cursor: "pointer",
                          color: item.isFavorite ? "#FF8539" : "#ccc",
                        }}
                        onClick={() => handleStarClick(index)}
                      />
                    </td>
                    <td>{index + 1}</td>
                    <td>{item["doc_number"]}</td>
                    <td>{item["doc_name"]}</td>
                    <td>{item["subject"]}</td>
                    <td>{item["department"]}</td>
                    <td>{item["doc_date"].split("T")[0]}</td>
                    <td>{item["doc_time"].split("T")[1].split(".")[0]}</td>

                    {/* ‚úÖ ‡πÉ‡∏™‡πà dropdown ‡πÉ‡∏ô <td> */}
                    <Dropdown>
                      <Dropdown.Toggle
                        variant="success"
                        id={`dropdown-${index}`}
                      >
                        <i className="bi bi-list"></i>
                      </Dropdown.Toggle>
                      <Dropdown.Menu>
                        {userRole === "admin" && (
                          <>
                            <Dropdown.Item
                              className="bi bi-pencil-square"
                              onClick={(e) => {
                                e.stopPropagation(); // ‡∏õ‡πâ‡∏≠‡∏á‡∏Å‡∏±‡∏ô‡∏Ñ‡∏•‡∏¥‡∏Å‡πÅ‡∏ñ‡∏ß
                                navigate("/addDoc", { state: { doc: item } });
                              }}
                            >
                              &nbsp;‡πÅ‡∏Å‡πâ‡πÑ‡∏Ç
                            </Dropdown.Item>
                            <Dropdown.Item
                              className="bi bi-box-arrow-down"
                              onClick={(e) => {
                                e.stopPropagation();
                                handleDownload(item["doc_number"]);
                              }}
                            >
                              &nbsp;Download
                            </Dropdown.Item>
                            <Dropdown.Item
                              className="bi bi-trash"
                              onClick={(e) => {
                                e.stopPropagation();
                                handleShowModal(item);
                              }}
                            >
                              &nbsp;‡∏•‡∏ö
                            </Dropdown.Item>
                          </>
                        )}
                        {(userRole === "worker" || userRole === "guest") && (
                          <Dropdown.Item
                            className="bi bi-box-arrow-down"
                            onClick={(e) => {
                              e.stopPropagation();
                              handleDownload(item["doc_number"]);
                            }}
                          >
                            &nbsp;Download
                          </Dropdown.Item>
                        )}
                      </Dropdown.Menu>
                    </Dropdown>
                  </tr>
                ))
              ) : (
                <tr>
                  <td colSpan="9">‡πÑ‡∏°‡πà‡∏û‡∏ö‡∏ú‡∏•‡∏•‡∏±‡∏û‡∏ò‡πå</td>
                </tr>
              )}
            </tbody>
          </table>
        </div>

        {/* ‡∏õ‡∏∏‡πà‡∏°‡∏≠‡∏±‡∏û‡πÇ‡∏´‡∏•‡∏î‡πÄ‡∏≠‡∏Å‡∏™‡∏≤‡∏£‡∏™‡∏≥‡∏´‡∏£‡∏±‡∏ö Admin */}
        {userRole === "admin" && <ButtonUpload />}

        {/* Modal ‡∏¢‡∏∑‡∏ô‡∏¢‡∏±‡∏ô‡∏Å‡∏≤‡∏£‡∏•‡∏ö */}
        <Actiondropdown
          show={showModal}
          onClose={() => setShowModal(false)}
          onConfirm={handleDelete}
          docName={selectedDoc?.["‡∏ä‡∏∑‡πà‡∏≠‡πÄ‡∏≠‡∏Å‡∏™‡∏≤‡∏£"]}
        />
      </div>
    </>
  );
};

export default Pagedoc;
